package interfaceex.inheritance;

public class MyClass implements MyInterface{

	@Override
	public void x() {
		System.out.println("x() 출력");
	}

	@Override
	public void y() {
		System.out.println("y() 출력");
	}

	@Override
	public void myMethod() {
		System.out.println("myMethod() 출력");
	}

	




}
